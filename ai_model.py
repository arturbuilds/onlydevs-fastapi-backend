training_labels = [0] * 50 + [1] * 50

def load_or_train_model():
    model_path = 'ai_model.pkl'

    if os.path.exists(model_path):
        print('🧠 Загружаем сохранённую модель ИИ...')
        return joblib.load(model_path)
    
    print('🧠 Обучаем новую модель ИИ...')
    model = make_pipeline(
        TfidfVectorizer(
            max_features=10000,
            ngram_range=(1, 4),
            lowercase=True
        ),
        MultinomialNB()
    )

    model.fit(training_text, training_labels)
    joblib.dump(model, model_path)
    print('✅ Модель ИИ успешно обучена и сохранена!')

    return model

ai_model = load_or_train_model()

def check_post_with_local_ai(title: str, content: str) -> bool:
    text_to_check = f'{title} {content}'.lower()

    prediction = ai_model.predict([text_to_check])[0]
    probabilities = ai_model.predict_proba([text_to_check])[0]

    spam_prob = probabilities[1]

    print(f"ИИ проверка: '{text_to_check[:60]}...' → Спам вероятность: {spam_prob:.1%}")

    return prediction == 0
